function delete_file(val,area,under,numb) {
    // alert(val);
    $.ajax({
        type: "POST",
        url: "../actions/upload6/delete_file.php",
        async: false,
        data: {
            val: val,
            area: area,
            under: under,
            numb: numb,
            delete: 1
        },
        success:function(result){
            alert(result);
            window.location.reload();
        }
    });
}
